/// 4px-base spacing scale. Use named constants instead of magic numbers
/// inside widgets so layout stays consistent across the app.
abstract class AppSpacing {
  static const double space1 = 4;
  static const double space2 = 8;
  static const double space3 = 12;
  static const double space4 = 16;
  static const double space5 = 20;
  static const double space6 = 24;
  static const double space8 = 32;
  static const double space10 = 40;
  static const double space12 = 48;
  static const double space16 = 64;
  static const double space20 = 80;

  // Page padding shortcut.
  static const double pageX = 20;
  static const double pageTop = 16;
  static const double pageBottom = 32;
}
